import SwiftUI

/// チップスタイル
///
/// チップコンポーネントの表示スタイルを定義する列挙型
public enum ChipStyle {
    case standard      // 標準
    case filled        // 塗りつぶし
    case outlined      // アウトライン
    case minimal       // ミニマル
    case pill          // ピル
    case rounded       // 丸角
}
